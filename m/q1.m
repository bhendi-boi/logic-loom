fs=1000;
w=0:1/fs:2*pi;
x=sin(w);
y=cos(w);
z=x./y;
subplot(2,3,1)
plot(w,x)
xlabel('x');
ylabel('sin(x)');
title('Sin');
subplot(2,3,2)
plot(w,y)
xlabel('x');
ylabel('cos(x)');
title('Cos');
subplot(2,3,3)
plot(w,z)
xlabel('x');
ylabel('tan(x)');
title('Tan');
subplot(2,3,4)
h=1./x;
plot(w,h)
xlabel('x');
ylabel('sec(x)');
title('Sec');
subplot(2,3,5)
j=1./y;
plot(w,j)
xlabel('x');
ylabel('cosec(x)');
title('Cosec');
subplot(2,3,6)
k=1./z;
plot(w,k)
xlabel('x');
ylabel('cot(x)');
title('Cot');


